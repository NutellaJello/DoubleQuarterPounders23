    public void turnNinety(int direction) { // 1 = clockwise, 2 = c-clockwise
        if (direction == 1) {
            FrontLeft.setPower(0.4);
            BackLeft.setPower(0.4);
            FrontRight.setPower(-0.4);
            BackRight.setPower(-0.4);
            sleep(1000);
        } else if (direction == 2) {
            FrontLeft.setPower(-0.4);
            BackLeft.setPower(-0.4);
            FrontRight.setPower(0.4);
            BackRight.setPower(0.4);
            sleep(1000);
        }

    }

    public void turnLeft(int move, double velocity) {
        FrontLeft.setMode(DcMotor.RunMode.STOP_AND_RESET_ENCODER);
        FrontLeft.setTargetPosition(1770);
        FrontLeft.setMode(DcMotor.RunMode.RUN_TO_POSITION);
        FrontLeft.setVelocity(velocity);

        BackLeft.setMode(DcMotor.RunMode.STOP_AND_RESET_ENCODER);
        BackLeft.setTargetPosition(1770);
        BackLeft.setMode(DcMotor.RunMode.RUN_TO_POSITION);
        BackLeft.setVelocity(velocity);

        FrontRight.setMode(DcMotor.RunMode.STOP_AND_RESET_ENCODER);
        FrontRight.setTargetPosition(-1770);
        FrontRight.setMode(DcMotor.RunMode.RUN_TO_POSITION);
        FrontRight.setVelocity(velocity);

        BackRight.setMode(DcMotor.RunMode.STOP_AND_RESET_ENCODER);
        BackRight.setTargetPosition(-1770);
        BackRight.setMode(DcMotor.RunMode.RUN_TO_POSITION);
        BackRight.setVelocity(velocity);
    }


    private void sendTelemetry(boolean straight) {

        if (straight) {
            telemetry.addData("Motion", "Drive Straight");
            telemetry.addData("Target Pos L:R", "%7d:%7d", leftTarget, rightTarget);
            telemetry.addData("Actual Pos L:R", "%7d:%7d", FrontLeft.getCurrentPosition(),
                    FrontRight.getCurrentPosition());
        } else {
            telemetry.addData("Motion", "Turning");
        }

        telemetry.addData("Error  : Steer Pwr", "%5.1f : %5.1f", headingError, turnSpeed);
        telemetry.addData("Wheel Speeds L : R", "%5.2f : %5.2f", flspeed, frspeed);
        telemetry.update();
    }
